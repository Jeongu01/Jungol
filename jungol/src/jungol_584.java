public class jungol_584 {
    public static void main(String[] args) {
        makeExpr();
    }

    public static void makeExpr() {
        for(int i = 1; i <= 3; i++) {
            for(int j = 1; j <= 3; j++) {
                System.out.println(i + " + " + j + " = " + (i + j));
            }
        }
    }
}
